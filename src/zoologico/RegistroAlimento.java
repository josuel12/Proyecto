    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
        
        
        String id = txtId.getText();
        Date fechaAlimentacion = txtFecha2.getDate();

        
        
        
        
        Date fechaActual = new Date();
        if (fechaAlimentacion != null && fechaAlimentacion.before(fechaActual)) {
            
            String fechaAlimentacionString = fechaAlimentacion.toString();
            registrarFechaAlimentacion(id, fechaAlimentacionString);
            mostrar();
            txtId.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Verifique los datos ingresados");
        }
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

