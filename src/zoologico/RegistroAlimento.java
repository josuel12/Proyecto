    public void mostrar(){
    String matriz [][] = new String[Animal.listaAnimal.size()][5];
    for (int i = 0; i <Animal.listaAnimal.size(); i++){
        matriz[i][0] = Animal.listaAnimal.get(i).getId();
        matriz[i][1] = Animal.listaAnimal.get(i).getNombre();      
        matriz[i][2] = Animal.listaAnimal.get(i).getEdad();
        matriz[i][3] = Animal.listaAnimal.get(i).getFechaAlim();
    }
    tableAlimento.setModel(new javax.swing.table.DefaultTableModel(
    matriz,
    new String []{
        "ID", "Nombre", "Edad", "Fecha Alimentacion"
    }
    
    ));
}
    public static void registrarFechaAlimentacion(String id, String fechaAlimentacion) {
        for (Animal animal : listaAnimal) {
            if (animal.getId().equals(id)) {
                animal.setFechaAlim(fechaAlimentacion);
                break;
            }else{
                JOptionPane.showMessageDialog(null, "ID Animal no existe");
            }
        }
    }

